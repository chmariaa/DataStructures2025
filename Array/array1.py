# Array with Basic Numerical Data
array = [10, 20, 30, 40, 50]

print("\nInitial array:", array)

print("\nElement at index 0:", array[0])
print("Element at index 2:", array[2]) 

array[3] = 100 
print("\nArray after updating element at index 3:", array)

print("\nIterating through the array:")
for element in array:
    print(element)
