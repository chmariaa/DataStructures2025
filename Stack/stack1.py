# Stack with Push and Pop Operations
stack = []

stack.append(10) 
stack.append(20) 
stack.append(30)

print("\nStack after pushing elements:", stack)

# Popping elements from the stack
popped_item = stack.pop() 
print("Popped item:", popped_item)
print("\nStack after popping one element:", stack)

popped_item = stack.pop()
print("Popped item:", popped_item)
print("\nStack after popping another element:", stack)
